create table if not exists public.prompt_bank (
  id uuid primary key default gen_random_uuid(),
  prompt_id text not null,
  module_id text not null default 'admin-prompts',
  module_title text not null default 'Admin Prompts',
  prompt_type text not null default 'translation'
    check (prompt_type in ('translation', 'elicitation', 'picture_description', 'validation', 'gamified')),
  dialect text,
  english text not null,
  transliteration text,
  media_url text,
  media_type text not null default 'none'
    check (media_type in ('none', 'image', 'audio')),
  difficulty text not null default 'short'
    check (difficulty in ('short', 'medium', 'long')),
  curriculum_stage text,
  grammatical_category text,
  weight integer not null default 1 check (weight >= 0),
  active boolean not null default true,
  sort_order integer not null default 0,
  created_by text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.prompt_correction_reviews (
  id uuid primary key default gen_random_uuid(),
  prompt_bank_id uuid references public.prompt_bank(id) on delete set null,
  module_id text not null,
  prompt_id text not null,
  previous_english text,
  accepted_correction text not null,
  candidate_corrections jsonb not null default '[]'::jsonb,
  accepted_feedback_id bigint references public.feedback(id) on delete set null,
  reviewed_by text,
  created_at timestamptz not null default now()
);

